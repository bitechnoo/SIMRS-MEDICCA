	public function run()
	{
		global $ExportType, $CustomExportType, $ExportFileName, $UserProfile, $Language, $Security, $RequestSecurity, $CurrentForm,
			$UserTableConn, $FormError, $Breadcrumb;

		// Init Session data for API request if token found
		if (IsApi() && session_status() !== PHP_SESSION_ACTIVE) {
			$func = PROJECT_NAMESPACE . CHECK_TOKEN_FUNC;
			if (is_callable($func) && Param(TOKEN_NAME) !== NULL && $func(Param(TOKEN_NAME), SessionTimeoutTime()))
				session_start();
		}

		// User profile
		$UserProfile = new UserProfile();

		// Security
		$Security = new AdvancedSecurity();
		$validRequest = FALSE;

		// Check security for API request
		If (IsApi()) {

			// Check token first
			$func = PROJECT_NAMESPACE . CHECK_TOKEN_FUNC;
			if (is_callable($func) && Post(TOKEN_NAME) !== NULL)
				$validRequest = $func(Post(TOKEN_NAME), SessionTimeoutTime());
			elseif (is_array($RequestSecurity) && @$RequestSecurity["username"] <> "") // Login user for API request
				$Security->loginUser(@$RequestSecurity["username"], @$RequestSecurity["userid"], @$RequestSecurity["parentuserid"], @$RequestSecurity["userlevelid"]);
		}
		if (!$validRequest) {
			if (!IsPasswordReset()) {
			if (!$Security->isLoggedIn()) $Security->autoLogin();
			if (!$Security->isLoggedIn() || $Security->isSysAdmin())
				$this->terminate(GetUrl("login.php"));
			$Security->loadCurrentUserLevel($this->ProjectID . 'vlokpetugas');
			}
		}
		$this->CurrentAction = Param("action"); // Set up current action

		// Global Page Loading event (in userfn*.php)
		Page_Loading();

		// Page Load event
		$this->Page_Load();

		// Check token
		if (!$this->validPost()) {
			Write($Language->phrase("InvalidPostRequest"));
			$this->terminate();
		}

		// Create Token
		$this->createToken();
		$Breadcrumb = new Breadcrumb();
		$Breadcrumb->add("changepwd", "ChangePwdPage", CurrentUrl(), "", "", TRUE);
		$this->Heading = $Language->phrase("ChangePwdPage");
		$postBack = IsPost();
		$validate = TRUE;
		if ($postBack) {
			$oldPassword = Post("opwd");
			$newPassword = Post("npwd");
			$confirmedPassword = Post("cpwd");
			$validate = $this->validateForm($oldPassword, $newPassword, $confirmedPassword);
			if (!$validate) {
				$this->setFailureMessage($FormError);
			}
		}
		$pwdUpdated = FALSE;
		if ($postBack && $validate) {

			// Setup variables
			$userName = $Security->currentUserName();
			if (IsPasswordReset())
				$userName = $_SESSION[SESSION_USER_PROFILE_USER_NAME];
			$filter = str_replace("%u", AdjustSql($userName, USER_TABLE_DBID), USER_NAME_FILTER);

			// Set up filter (WHERE Clause)
			$this->CurrentFilter = $filter;
			$sql = $this->getCurrentSql();
			if ($rs = $UserTableConn->execute($sql)) {
				if (!$rs->EOF) {
					$rsold = $rs->fields;
					if (IsPasswordReset() || ComparePassword($rsold['Password'], $oldPassword)) {
						$validPwd = TRUE;
						if (!IsPasswordReset())
							$validPwd = $this->User_ChangePassword($rsold, $userName, $oldPassword, $newPassword);
						if ($validPwd) {
							$rsnew = array('Password' => $newPassword); // Change Password
							$rs->close();
							$UserTableConn->raiseErrorFn = $GLOBALS["ERROR_FUNC"];
							$validPwd = $this->update($rsnew);
							$UserTableConn->raiseErrorFn = '';
							if ($validPwd)
								$pwdUpdated = TRUE;
						} else {
							$this->setFailureMessage($Language->phrase("InvalidNewPassword"));
							$rs->close();
						}
					} else {
						$this->setFailureMessage($Language->phrase("InvalidPassword"));
					}
				} else {
					$rs->close();
				}
			}
		}
		if ($pwdUpdated) {
			if ($this->getSuccessMessage() == "")
				$this->setSuccessMessage($Language->phrase("PasswordChanged")); // Set up success message
			if (IsPasswordReset()) {
				$_SESSION[SESSION_STATUS] = "";
				$_SESSION[SESSION_USER_PROFILE_USER_NAME] = "";
			}
			$this->terminate("index.php"); // Return to default page
		}
	}

	// Validate form
	protected function validateForm($opwd, $npwd, $cpwd)
	{
		global $Language, $FormError;

		// Check if validation required
		if (!SERVER_VALIDATE)
			return TRUE;

		// Initialize form error message
		$FormError = "";
		if (!IsPasswordReset() && $opwd == "") {
			AddMessage($FormError, $Language->phrase("EnterOldPassword"));
		}
		if ($npwd == "") {
			AddMessage($FormError, $Language->phrase("EnterNewPassword"));
		}
		if ($npwd <> $cpwd) {
			AddMessage($FormError, $Language->phrase("MismatchPassword"));
		}

		// Return validate result
		$valid = ($FormError == "");

		// Call Form CustomValidate event
		$formCustomError = "";
		$valid = $valid && $this->Form_CustomValidate($formCustomError);
		if ($formCustomError <> "") {
			AddMessage($FormError, $formCustomError);
		}
		return $valid;
	}

