	public function run()
	{
		global $ExportType, $CustomExportType, $ExportFileName, $UserProfile, $Language, $Security, $RequestSecurity, $CurrentForm,
			$Breadcrumb;

		// Init Session data for API request if token found
		if (IsApi() && session_status() !== PHP_SESSION_ACTIVE) {
			$func = PROJECT_NAMESPACE . CHECK_TOKEN_FUNC;
			if (is_callable($func) && Param(TOKEN_NAME) !== NULL && $func(Param(TOKEN_NAME), SessionTimeoutTime()))
				session_start();
		}

		// User profile
		$UserProfile = new UserProfile();

		// Security
		$Security = new AdvancedSecurity();
		$validRequest = FALSE;

		// Check security for API request
		If (IsApi()) {

			// Check token first
			$func = PROJECT_NAMESPACE . CHECK_TOKEN_FUNC;
			if (is_callable($func) && Post(TOKEN_NAME) !== NULL)
				$validRequest = $func(Post(TOKEN_NAME), SessionTimeoutTime());
			elseif (is_array($RequestSecurity) && @$RequestSecurity["username"] <> "") // Login user for API request
				$Security->loginUser(@$RequestSecurity["username"], @$RequestSecurity["userid"], @$RequestSecurity["parentuserid"], @$RequestSecurity["userlevelid"]);
		}
		if (!$validRequest) {
		}

		// Global Page Loading event (in userfn*.php)
		Page_Loading();

		// Page Load event
		$this->Page_Load();

		// Check token
		if (!$this->validPost()) {
			Write($Language->phrase("InvalidPostRequest"));
			$this->terminate();
		}

		// Create Token
		$this->createToken();
		$Breadcrumb = new Breadcrumb();

		// If session expired, show session expired message
		if (Get("expired") == "1")
			$this->setFailureMessage($Language->phrase("SessionExpired"));
		if (!$Security->isLoggedIn())
			$Security->autoLogin();
		$Security->loadUserLevel(); // Load User Level
		if ($Security->allowList(CurrentProjectID() . 'lokpasien'))
			$this->terminate("lokpasienlist.php"); // Exit and go to default page
		if ($Security->allowList(CurrentProjectID() . 'lokkelurahan'))
			$this->terminate("lokkelurahanlist.php");
		if ($Security->allowList(CurrentProjectID() . 'lokbiayadaftar'))
			$this->terminate("lokbiayadaftarlist.php");
		if ($Security->allowList(CurrentProjectID() . 'lokdaftar'))
			$this->terminate("lokdaftarlist.php");
		if ($Security->allowList(CurrentProjectID() . 'lokpoliklinik'))
			$this->terminate("lokpolikliniklist.php");
		if ($Security->allowList(CurrentProjectID() . 'vlokpetugas'))
			$this->terminate("vlokpetugaslist.php");
		if ($Security->allowList(CurrentProjectID() . 'lokuserlevelpermissions'))
			$this->terminate("lokuserlevelpermissionslist.php");
		if ($Security->allowList(CurrentProjectID() . 'lokuserlevels'))
			$this->terminate("lokuserlevelslist.php");
		if ($Security->allowList(CurrentProjectID() . 'kartu_fargo.php'))
			$this->terminate("./kartu_fargo.php");
		if ($Security->allowList(CurrentProjectID() . 'ugdbiayadaftar'))
			$this->terminate("ugdbiayadaftarlist.php");
		if ($Security->allowList(CurrentProjectID() . 'lokasalrujukan'))
			$this->terminate("lokasalrujukanlist.php");
		if ($Security->allowList(CurrentProjectID() . 'lokjenispasien'))
			$this->terminate("lokjenispasienlist.php");
		if ($Security->allowList(CurrentProjectID() . 'ugddaftar'))
			$this->terminate("ugddaftarlist.php");
		if ($Security->isLoggedIn()) {
			$this->setFailureMessage(DeniedMessage() . "<br><br><a href=\"logout.php\">" . $Language->phrase("BackToLogin") . "</a>");
		} else {
			$this->terminate("login.php"); // Exit and go to login page
		}
	}

